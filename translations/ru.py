# Функция для генерации файла перевода
def generate_qm_file(output_file):
    """
    Генерирует файл .qm с переводами в простом текстовом формате
    """
    try:
        with open(output_file, "w", encoding="utf-8") as f:
            f.write("# Russian translation\n")
            for source, translation in translations.items():
                f.write(f"{source}={translation}\n")
        
        print(f"Перевод успешно сохранен в файл: {output_file}")
        
    except Exception as e:
        print(f"Ошибка при создании файла перевода: {e}")

